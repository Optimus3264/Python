# Define two numbers
a = 10
b = 20

print("a =", a)
print("b =", b)

# Equal to (==)
print("a == b:", a == b)  # False

# Not equal to (!=)
print("a != b:", a != b)  # True

# Greater than (>)
print("a > b:", a > b)    # False

# Less than (<)
print("a < b:", a < b)    # True

# Greater than or equal to (>=)
print("a >= b:", a >= b)  # False

# Less than or equal to (<=)
print("a <= b:", a <= b)  # True

